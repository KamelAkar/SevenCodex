const MOJIBAKE_HINTS = ["Ã", "â€™", "â€œ", "â€", "Â"];
const REPAIRED_LOCALES = new Map();

function repairMojibakeString(value) {
  const text = String(value ?? "");
  if (!MOJIBAKE_HINTS.some((hint) => text.includes(hint))) {
    return text;
  }
  try {
    const bytes = Uint8Array.from([...text].map((char) => char.charCodeAt(0) & 0xff));
    const repaired = new TextDecoder("utf-8").decode(bytes);
    return repaired.includes("\ufffd") ? text : repaired;
  } catch {
    return text;
  }
}

function deepRepair(node) {
  if (typeof node === "string") {
    return repairMojibakeString(node);
  }
  if (Array.isArray(node)) {
    return node.map((value) => deepRepair(value));
  }
  if (!node || typeof node !== "object") {
    return node;
  }
  return Object.fromEntries(Object.entries(node).map(([key, value]) => [key, deepRepair(value)]));
}

export function getLocale(language) {
  const resolved = UI_LOCALES[language] ? language : DEFAULT_LANGUAGE;
  if (!REPAIRED_LOCALES.has(resolved)) {
    REPAIRED_LOCALES.set(resolved, deepRepair(UI_LOCALES[resolved]));
  }
  return REPAIRED_LOCALES.get(resolved);
}

export function t(language, key, vars = {}) {
  const source = getLocale(language);
  const fallback = UI_LOCALES[DEFAULT_LANGUAGE];
  const value =
    String(key || "")
      .split(".")
      .reduce((acc, part) => (acc && typeof acc === "object" ? acc[part] : undefined), source) ??
    String(key || "")
      .split(".")
      .reduce((acc, part) => (acc && typeof acc === "object" ? acc[part] : undefined), fallback) ??
    key;
  if (typeof value !== "string") {
    return String(value ?? key);
  }
  return value.replace(/\{(\w+)\}/g, (_, token) => String(vars[token] ?? ""));
}
